// Q-2: null vs undefined

console.log(typeof null); // object
console.log(typeof undefined); // undefined

// undefined is the variable declared but not initialized.

// null can be a actual value.

console.log(null == undefined); // true
console.log(null === undefined); //false

// '==' compares both of the entity without matching their types.
// '===' compares the types of both of these entities as well.